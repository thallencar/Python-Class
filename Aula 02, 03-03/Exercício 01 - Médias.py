"""
Procedimento: 
- Obter o primeiro número
- Obter o segundo número
- Calcular a média
- Exibir o resultado
"""
#Obter o primero número (Entrada de dados)
nota1 = float(input("Insira a primeira nota: "))
#Obter o segundo número (Entrada de dados)
nota2 = float(input("Insira a segunda nota: "))
#Calcular a média (Processamento de dados)
media = (nota1+nota2)/2
#Exibir o resultado (Saída de dados)
print("A média obtida foi: ", media)

#input sempre é um dado do tipo texto, ao qual o número não é preestabelecido
